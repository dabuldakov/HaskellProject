--------------------------------ЛАБОРАТОРНАЯ РАБОТА 1. ОСНОВЫ ЯЗЫКА ПРОЛОГ. СОЗДАНИЕ ПРОСТЕЙШИХ ФУНКЦИЙ

odd' []       = []
odd' (x:[])   = []
odd' (x:y:xs) = y : odd' xs

---------------------------------ЛАБОРАТОРНАЯ РАБОТА 2. РАЗРАБОТКА ПРОГРАММ.

replace' []     []     list = list
replace' (x:xs) (y:ys) list = replace' xs ys (map (\a -> if a == x then y else a) list)

